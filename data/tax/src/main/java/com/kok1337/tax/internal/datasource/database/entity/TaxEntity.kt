package com.kok1337.tax.internal.datasource.database.entity

import com.kok1337.database.api.annotation.Column
import com.kok1337.database.api.annotation.Entity
import com.kok1337.tax.api.TaxApiModel
import java.sql.Date
import java.util.*

@Entity
internal class TaxEntity(
    @Column("id") var id: UUID? = null,
    @Column("locality_id") var localityId: UUID? = null,
    @Column("section") var section: String? = null,
    @Column("forest_purpose_id") var forestPurposeId: Int? = null,
    @Column("protection_category_id") var protectionCategoryId: Int? = null,
    @Column("ozu_id") var ozuId: Int? = null,
    @Column("oopt_id") var ooptId: Int? = null,
    @Column("is_water_protection_zone") var isWaterProtectionZone: Boolean? = null,
    @Column("tenant") var tenant: String? = null,
    @Column("lease_contract_num") var leaseContractNum: String? = null,
    @Column("lease_contract_date") var leaseContractDate: Date? = null,
    @Column("lease_type_id") var leaseTypeId: Int? = null,
    @Column("cadastral_num") var cadastralNum: String? = null,
    @Column("fire_prescription") var firePrescription: Int? = null,
    @Column("forest_use_id") var forestUseId: Int? = null,
    @Column("s") var s: Double? = null,
    @Column("forest_inventory_year") var forestInventoryYear: Int? = null,
    @Column("forest_type") var forestType: String? = null,
    @Column("tlu_id") var tluId: Int? = null,
    @Column("land_cat_id") var landCatId: Int? = null,
    @Column("bonitet_id") var bonitetId: Int? = null,
    @Column("underwood") var underwood: String? = null,
    @Column("is_natural") var isNatural: Boolean? = null,
    @Column("lease_contract_type_id") var leaseContractTypeId: Int? = null,
    @Column("forest_zone_id") var forestZoneId: Int? = null,
    @Column("is_artificial") var isArtificial: Boolean? = null,
    @Column("planting_year") var plantingYear: Int? = null,
    @Column("is_after_tapping") var isAfterTapping: Boolean? = null,
    @Column("tab_zone_id") var tabZoneId: Int? = null,
    @Column("tlu") var tlu: String? = null,
    @Column("is_drained") var isDrained: Boolean? = null,
    @Column("age_group_id") var ageGroupId: Int? = null,
    @Column("is_fire_burned") var isFireBurned: Boolean? = null,
    @Column("radioactive_zone_id") var radioactiveZoneId: Int? = null,
    @Column("stock_dead") var stockDead: Double? = null,
    @Column("stock_open_stand") var stockOpenStand: Double? = null,
    @Column("stock_single_tree") var stockSingleTree: Double? = null,
    @Column("stock_felling_debris") var stockFellingDebris: Double? = null,
    @Column("stock_liquid_debris") var stockLiquidDebris: Double? = null,
    @Column("non_forest_land_id") var nonForestLandId: Int? = null,
    @Column("unforested_land_id") var unforestedLandId: Int? = null,
    @Column("is_draft") var isDraft: Boolean? = null,
    @Column("data_source_id") var dataSourceId: Int? = null,
    @Column("is_new_dead") var isNewDead: Boolean? = null,
    @Column("oopt") var oopt: String? = null,
    @Column("ozu") var ozu: String? = null,
    @Column("forest_use") var forestUse: String? = null,
    @Column("stock_section") var stockSection: Double? = null,
) {
    fun toModel(): TaxApiModel = TaxApiModel(
        id!!,
        localityId!!,
        section!!,
        forestPurposeId,
        protectionCategoryId,
        ozuId,
        ooptId,
        isWaterProtectionZone,
        tenant,
        leaseContractNum,
        leaseContractDate,
        leaseTypeId,
        cadastralNum,
        firePrescription,
        forestUseId,
        s,
        forestInventoryYear,
        forestType,
        tluId,
        landCatId,
        bonitetId,
        underwood,
        isNatural,
        leaseContractTypeId,
        forestZoneId,
        isArtificial,
        plantingYear,
        isAfterTapping,
        tabZoneId,
        tlu,
        isDrained,
        ageGroupId,
        isFireBurned,
        radioactiveZoneId,
        stockDead,
        stockOpenStand,
        stockSingleTree,
        stockFellingDebris,
        stockLiquidDebris,
        nonForestLandId,
        unforestedLandId,
        isDraft ?: true,
        dataSourceId,
        isNewDead,
        oopt,
        ozu,
        forestUse,
        stockSection,
    )
}